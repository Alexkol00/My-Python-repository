operand1 = int(input("Введите первый операнд: "))
operand2 = int(input("Введите второй операнд: "))

print(f"Сумма: {operand1} + {operand2} = {operand1 + operand2}")
print(f"Разность: {operand1} - {operand2} = {operand1 - operand2}")
print(f"Произведение: {operand1} * {operand2} = {operand1 * operand2}")
print(f"Частное: {operand1} / {operand2} = {operand1 / operand2}")
print(f"Целочисленное деление: {operand1} // {operand2} = {operand1 // operand2}")
print(f"Остаток от деления: {operand1} % {operand2} = {operand1 % operand2}")